// Contest started 90 min ago. Timestamps are relative to that.
// minutesIn: how many minutes after contest start this submission was made

const START = new Date(Date.now() - 90 * 60 * 1000)

function t(minutesIn) {
  return new Date(START.getTime() + minutesIn * 60 * 1000).toISOString()
}

let _id = 1
